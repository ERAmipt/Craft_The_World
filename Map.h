	class Block
	{
	public:

		Block(std::string name, sf::Vector2f tileset_coords, bool hard) :
			tileset_coords_(tileset_coords),
			hard_(hard),
			name_(name)
		{}

		float getX() { return tileset_coords_.x; };
		float getY() { return tileset_coords_.y; };
		bool  isSoft() { return !hard_; };

	private:
		sf::Vector2f tileset_coords_;
		bool hard_;
		std::string name_;
	};

