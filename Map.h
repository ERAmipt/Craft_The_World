	class Map : public sf::Drawable, public sf::Transformable
	{
	public:

		Map();

		/////////////////////////////////////////////////////
		// Get view of the map, which can be modified by mouse
		//   scrolling
		//
		// Usage: window.setView(M::map.getView());
		/////////////////////////////////////////////////////
		sf::View& getView() {
			return view_;
		}

		/////////////////////////////////////////////////////
		// Should be called every time user scroll the mouse
		//
		// Usage: M::ScrollMap(M::map, event.mouseWheel.delta, 
		//	    		event.mouseWheel.x, event.mouseWheel.y);
		/////////////////////////////////////////////////////
		friend void ScrollMap(Map& map, int delta, float x, float y);

		/////////////////////////////////////////////////////
		// Check is block is soft
		/////////////////////////////////////////////////////
		bool isSoft(int x, int y) const;
	private:

		virtual void draw(sf::RenderTarget& target, sf::RenderStates states) const;

		sf::VertexArray vertices_;
		sf::Texture tileset_;
		sf::View view_;
	};

